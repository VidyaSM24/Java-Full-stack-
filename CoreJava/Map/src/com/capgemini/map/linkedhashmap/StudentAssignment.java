package com.capgemini.map.linkedhashmap;

public class StudentAssignment {
	int age;
	String name;
	
	public StudentAssignment( int age,String name) {
		super();
		this.age = age;
		this.name = name;
		
	}

	@Override
	public String toString() {
		return "StudentAssignment [age=" + age + ", name=" + name + "]";
	};
	

}
